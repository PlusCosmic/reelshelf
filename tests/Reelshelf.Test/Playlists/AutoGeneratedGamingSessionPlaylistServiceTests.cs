using Reelshelf.Core;
using Reelshelf.Discord;
using Reelshelf.Games;
using Reelshelf.Playlists;
using Reelshelf.Playlists.Models;
using Xunit;

namespace Reelshelf.Test.Playlists;

public class AutoGeneratedGamingSessionPlaylistServiceTests
{
    [Fact]
    public async Task EnsureGamingSessionPlaylist_ReusesExistingSessionIdentityAfterRename()
    {
        FakeGamingSessionPlaylistStore store = new();
        AutoGeneratedGamingSessionPlaylistService service = new(store);
        Guid clipId = store.AddClip();

        PlaylistWithDetails first = await service.EnsureGamingSessionPlaylist(
            store.Category.Id,
            new DateOnly(2026, 5, 10),
            [clipId],
            "Europe/Berlin",
            store.User.DiscordId);

        store.RenamePlaylist(first.Id, "Ranked night");

        PlaylistWithDetails second = await service.EnsureGamingSessionPlaylist(
            store.Category.Id,
            new DateOnly(2026, 5, 10),
            [clipId],
            "Europe/Berlin",
            store.User.DiscordId);

        Assert.Equal(first.Id, second.Id);
        Assert.Equal("Ranked night", second.Name);
        Assert.Single(second.Clips);
        Assert.Equal(1, store.CreatedPlaylistCount);
    }

    [Fact]
    public async Task EnsureGamingSessionPlaylist_IgnoresDuplicateClipAdds()
    {
        FakeGamingSessionPlaylistStore store = new();
        AutoGeneratedGamingSessionPlaylistService service = new(store);
        Guid clipId = store.AddClip();

        PlaylistWithDetails playlist = await service.EnsureGamingSessionPlaylist(
            store.Category.Id,
            new DateOnly(2026, 5, 10),
            [clipId, clipId],
            "Europe/Berlin",
            store.User.DiscordId);

        PlaylistWithDetails second = await service.EnsureGamingSessionPlaylist(
            store.Category.Id,
            new DateOnly(2026, 5, 10),
            [clipId],
            "Europe/Berlin",
            store.User.DiscordId);

        Assert.Single(playlist.Clips);
        Assert.Single(second.Clips);
        Assert.Equal(1, store.AddClipCallCount);
    }

    [Fact]
    public async Task EnsureGamingSessionPlaylist_RejectsClipFromDifferentCategory()
    {
        FakeGamingSessionPlaylistStore store = new();
        AutoGeneratedGamingSessionPlaylistService service = new(store);
        Guid clipId = store.AddClip(gameCategoryId: Guid.NewGuid());

        await Assert.ThrowsAsync<Reelshelf.Exceptions.BadRequestException>(() =>
            service.EnsureGamingSessionPlaylist(
                store.Category.Id,
                new DateOnly(2026, 5, 10),
                [clipId],
                "Europe/Berlin",
                store.User.DiscordId));
    }

    private sealed class FakeGamingSessionPlaylistStore : IGamingSessionPlaylistStore
    {
        private readonly Dictionary<Guid, PlaylistRecord> _playlists = [];
        private readonly Dictionary<(Guid OwnerId, Guid CategoryId, DateOnly SessionDate), Guid> _sessions = [];
        private readonly Dictionary<Guid, ClipsStatements.ClipRow> _clips = [];
        private readonly Dictionary<Guid, List<PlaylistClip>> _playlistClips = [];

        public DiscordStatements.DiscordUserRow User { get; } = new()
        {
            Id = Guid.NewGuid(),
            DiscordId = "discord-user",
            Username = "clipper"
        };

        public GameCategory Category { get; } = new()
        {
            Id = Guid.NewGuid(),
            Name = "Apex Legends",
            Slug = "apex-legends"
        };

        public int CreatedPlaylistCount { get; private set; }
        public int AddClipCallCount { get; private set; }

        public Guid AddClip(Guid? ownerId = null, Guid? gameCategoryId = null)
        {
            Guid clipId = Guid.NewGuid();
            _clips[clipId] = new ClipsStatements.ClipRow
            {
                Id = clipId,
                OwnerId = ownerId ?? User.Id,
                GameCategoryId = gameCategoryId ?? Category.Id,
                VideoId = Guid.NewGuid(),
                CreatedAt = DateTimeOffset.UtcNow,
                Title = "Clip"
            };

            return clipId;
        }

        public void RenamePlaylist(Guid playlistId, string name)
        {
            PlaylistRecord playlist = _playlists[playlistId];
            _playlists[playlistId] = playlist with { Name = name };
        }

        public Task<DiscordStatements.DiscordUserRow> GetUser(string discordUserId)
        {
            return Task.FromResult(User);
        }

        public Task<GameCategory?> GetCategory(Guid categoryId)
        {
            return Task.FromResult(categoryId == Category.Id ? Category : null);
        }

        public Task<ClipsStatements.ClipRow?> GetClip(Guid clipId)
        {
            _clips.TryGetValue(clipId, out ClipsStatements.ClipRow? clip);
            return Task.FromResult(clip);
        }

        public Task<GamingSessionPlaylistRow?> GetGamingSessionPlaylist(Guid ownerId, Guid categoryId, DateOnly sessionDate)
        {
            if (!_sessions.TryGetValue((ownerId, categoryId, sessionDate), out Guid playlistId))
            {
                return Task.FromResult<GamingSessionPlaylistRow?>(null);
            }

            PlaylistRecord playlist = _playlists[playlistId];
            return Task.FromResult<GamingSessionPlaylistRow?>(new GamingSessionPlaylistRow(
                playlistId,
                ownerId,
                categoryId,
                sessionDate,
                playlist.Timezone));
        }

        public Task UpdateGamingSessionTimezone(Guid playlistId, string timezone)
        {
            PlaylistRecord playlist = _playlists[playlistId];
            _playlists[playlistId] = playlist with { Timezone = timezone };
            return Task.CompletedTask;
        }

        public Task<Playlist> CreatePlaylist(string name, string? description, string discordUserId)
        {
            Guid playlistId = Guid.NewGuid();
            CreatedPlaylistCount++;
            _playlists[playlistId] = new PlaylistRecord(playlistId, name, description, string.Empty);
            _playlistClips[playlistId] = [];

            return Task.FromResult(new Playlist(
                playlistId,
                name,
                description,
                User.Id,
                DateTimeOffset.UtcNow,
                DateTimeOffset.UtcNow));
        }

        public Task<Guid> UpsertGamingSessionPlaylist(Guid playlistId, Guid ownerId, Guid categoryId, DateOnly sessionDate, string timezone)
        {
            var key = (ownerId, categoryId, sessionDate);
            if (_sessions.TryGetValue(key, out Guid existingPlaylistId))
            {
                PlaylistRecord existing = _playlists[existingPlaylistId];
                _playlists[existingPlaylistId] = existing with { Timezone = timezone };
                return Task.FromResult(existingPlaylistId);
            }

            _sessions[key] = playlistId;
            PlaylistRecord playlist = _playlists[playlistId];
            _playlists[playlistId] = playlist with { Timezone = timezone };
            return Task.FromResult(playlistId);
        }

        public Task DeletePlaylist(Guid playlistId)
        {
            _playlists.Remove(playlistId);
            _playlistClips.Remove(playlistId);
            return Task.CompletedTask;
        }

        public Task<int> GetMaxPosition(Guid playlistId)
        {
            int max = _playlistClips[playlistId].Count == 0
                ? -1
                : _playlistClips[playlistId].Max(clip => clip.Position);
            return Task.FromResult(max);
        }

        public Task<bool> ClipExistsInPlaylist(Guid playlistId, Guid clipId)
        {
            return Task.FromResult(_playlistClips[playlistId].Any(clip => clip.ClipId == clipId));
        }

        public Task AddClipToPlaylist(Guid playlistId, Guid clipId, Guid addedByUserId, int position)
        {
            AddClipCallCount++;
            _playlistClips[playlistId].Add(new PlaylistClip(
                Guid.NewGuid(),
                clipId,
                position,
                addedByUserId,
                DateTimeOffset.UtcNow));
            return Task.CompletedTask;
        }

        public Task TouchPlaylistUpdatedAt(Guid playlistId)
        {
            return Task.CompletedTask;
        }

        public Task<PlaylistWithDetails?> GetPlaylistDetails(Guid playlistId, string discordUserId)
        {
            if (!_playlists.TryGetValue(playlistId, out PlaylistRecord? playlist))
            {
                return Task.FromResult<PlaylistWithDetails?>(null);
            }

            return Task.FromResult<PlaylistWithDetails?>(new PlaylistWithDetails(
                playlistId,
                playlist.Name,
                playlist.Description,
                User.Id,
                DateTimeOffset.UtcNow,
                DateTimeOffset.UtcNow,
                [],
                _playlistClips[playlistId].ToList()));
        }

        private sealed record PlaylistRecord(Guid Id, string Name, string? Description, string Timezone);
    }
}
